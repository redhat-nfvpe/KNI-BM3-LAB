#!/bin/bash 

PROV_IP_CIDR_DEFAULT "172.22.0.0/24"
BM_IP_CIDR_DEFAULT "192.168.111.0/24"

# shellcheck disable=SC1091
source "scripts/utils.sh"

# DO NOT CHANGE BELOW THIS LINE

export PROV_IP_ADDR
PROV_IP_ADDR=$(nthhost "${PROV_IP_CIDR:-PROV_IP_CIDR_DEFAULT}" 1)
export PROV_IP_ADDR
PROV_IP_IPXE_URL=$(nthhost "${BM_IP_CIDR:-PROV_IP_CIDR_DEFAULT}" 10)": 8080" # 172.22.0.10
export PROV_IP_IPXE_URL
PROV_IP_RANGE_START=$(nthhost "${BM_IP_CIDR:-PROV_IP_CIDR_DEFAULT}" 11)    # 172.22.0.11
export PROV_IP_RANGE_START
PROV_IP_RANGE_END=$(nthhost "${BM_IP_CIDR:-PROV_IP_CIDR_DEFAULT}" 30)      # 172.22.0.30
export PROV_IP_RANGE_END

export BM_IP_CIDR
BM_IP_RANGE_START=$(nthhost "${BM_IP_CIDR:-BM_IP_CIDR_DEFAULT}" 10)  # 192.168.111.10
export BM_IP_RANGE_START
BM_IP_RANGE_END=$(nthhost "${BM_IP_CIDR:-BM_IP_CIDR_DEFAULT}" 60)    # 192.168.111.60
export BM_IP_RANGE_END
BM_IP_BOOTSTRAP=$(nthhost "${BM_IP_CIDR:-BM_IP_CIDR_DEFAULT}" 10)    # 192.168.111.10
export BM_IP_BOOTSTRAP

BM_IP_NS=$(nthhost "${BM_IP_CIDR:-BM_IP_CIDR_DEFAULT}" 1)            # 192.168.111.1
export BM_IP_NS
BM_IP_WORKER_START=$(nthhost "${BM_IP_CIDR:-BM_IP_CIDR_DEFAULT}" 20) # 192.168.111.20
export BM_IP_WORKER_START
